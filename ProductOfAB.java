import java.util.*;

public class ProductOfAB {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter a");
        int a = sc.nextInt();
        System.out.println("Enter b");
        int b = sc.nextInt();
        int product = a * b;
        System.out.println("a*b= " + product);

    }

}
