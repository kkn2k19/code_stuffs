public class Output {
    public static void main(String args[]) {
        // printing a line
        System.out.print("Hello World");

        // printing more lines
        System.out.print("Hello World");
        System.out.print("Hello World");
        // here after printing we got output in same line lets see how we print in
        // different lines

        // we need use println function to get two different lines
        System.out.println("Hello World");
        System.out.println("Hello World");
        System.out.println("Hello World");

        // we can also use "\n" function to break or end a line
        System.out.println("Hello World\nHello world");
        System.out.println("Hello World\nHello world");
        System.out.println("Hello World\nHello world");
        System.out.println("Hello World\nHello world");

    }
}
